    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
        	
        	//選不同的頁面就建不同的碎片
        	
        	Fragment fragment;
        	Bundle args = new Bundle();
        	args.putInt(ActivityReceive.ARG_SECTION_NUMBER, position + 1);
        	//傳bundle給碎片(目前所選的頁面位置+1，以確認所在頁面)
        	
        	switch (position) {
        		case 0:
        			fragment = new ActivityReceive();
                    fragment.setArguments(args);
        			return fragment;
        		case 1:
        			fragment = new ActivityPush();
                    fragment.setArguments(args);
        			return fragment;
        		case 2:
        			fragment = new ActivityFiles();
                    fragment.setArguments(args);
        			return fragment;
        		case 3:
        			fragment = new ActivityCollection();
                    fragment.setArguments(args);
        			return fragment;
        		case 4:
        			fragment = new ActivitySet();
                    fragment.setArguments(args);
        			return fragment;
        	}
        	return null;
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 5;
        }

        @Override
        public CharSequence getPageTitle(int position) {
        	
        	//設定頁籤標題
        	
            Locale l = Locale.getDefault();
            switch (position) {
                case 0:
                    return getString(R.string.title_section1).toUpperCase(l);
                case 1:
                    return getString(R.string.title_section2).toUpperCase(l);
                case 2:
                    return getString(R.string.title_section3).toUpperCase(l);
                case 3:
                    return getString(R.string.title_section4).toUpperCase(l);
                case 4:
                    return getString(R.string.title_section5).toUpperCase(l);
            }
            return null;
        }
    }
    
